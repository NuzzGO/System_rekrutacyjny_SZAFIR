Jak założyć konto i złożyć aplikację na wybrane stanowisko?
<br>
<br>
Witaj! Dziękujemy, że nas wybrałeś.
<br>
Aby pomóc Ci w rejestracji, przedstawiamy kilka wskazówek, jak korzystać z naszego formularza rejestracyjnego:
<br>
<br>
1. Uzupełnij pola wymagane: Formularz rejestracyjny zawiera pola wymagane, które muszą być uzupełnione,
<br>
   aby przejść do następnej strony. Są to pola: imię, nazwisko, adres e-mail oraz hasło. 
<br>
   Upewnij się, że wszystkie pola wymagane zostały wypełnione przed kliknięciem przycisku "Zarejetruj".
<br>
<br>
2. Zaloguj się.
<br>
<br>
3. Po zalogowaniu pojawia się możliwość zaaplikowania na wybrane stanowisko 
<br>
Wybierz preferencje: po otwarciu formularza rejestracyjnego znajdziesz pole z preferencjami dotyczącymi stanowiska
<br>
oraz pola do uzupełnienia informacjami o swoich umiejętnościach i przesłania CV w formacie .pdf.
<br>
Wybierz preferowane stanowisko, wypisz umiejętności, dodaj CV i kliknij "Zaaplikuj" w celu złożenia aplikacji
<br>
na wybrane stanowisko.
<br>
<br>
4. Gotowe. Informacje dotyczące procesu rekrutacji przesyłane będą drogą mailową na adres podany przy rejestracji.
<br>
<br>
Jeśli potrzebujesz dodatkowej pomocy, możesz skontaktować się z naszym zespołem obsługi klienta, 
<br>
wysyłając e-mail na adres [adres e-mail], lub dzwoniąc pod numer [numer telefonu]. 
<br>
Jesteśmy tu, aby Ci pomóc i odpowiedzieć na wszystkie Twoje pytania!
